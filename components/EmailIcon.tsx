/**
 * Inline envelope SVG used as the visual affordance for emailing a contact.
 *
 * Decorative by default (`aria-hidden`): the surrounding link supplies the
 * accessible name, so the icon must not duplicate it for screen readers.
 */
export function EmailIcon({ size = 20 }: { size?: number }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      focusable="false"
    >
      <rect x="2" y="4" width="20" height="16" rx="2" />
      <path d="m2 7 10 6 10-6" />
    </svg>
  );
}
